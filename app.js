const pg = new Object({
  sequence: 0,
  position: 'right',
  state: new Array(),

  set background({ position, hex }) {
    document.body.style.backgroundImage = `linear-gradient(to ${position || this.position}, ${(hex)? hex.join(','): this.state[2]})`;
  },

  switchBackgroundPosition() {
    const supportpos = ['left', 'top', 'right', 'bottom'];
    const index = supportpos.indexOf(this.position);

    if (index !== -1) {
      const newindex = (index >= supportpos.length - 1)? 0: (index + 1);
      this.position = supportpos[newindex];
      this.background = {
        position: this.position
      };
      return this.position;
    }
    return null;
  },

  /*
   *  @param {Object} gradientElement
   *  @return {String} id
   */
  apply(instance) {
    const [ id, cname, hex ] = instance;
    // For init access
    window.location.hash = id;
    document.getElementById('present-name').textContent = cname;
    this.background = { hex };
    return id;
  },
  supply(i) {
    this.apply(this.state = gradients[i || this.sequence]);
  },
  next() {
    this.supply(this.sequence = (this.sequence >= gradients.length - 1)? 0: ++this.sequence);
  },
  previous() {
    this.supply(this.sequence = (this.sequence <= 0)? gradients.length - 1: --this.sequence);
  },
  applyFromHashValue() {
    const hashval = window.location.hash.substr(1);
    const index = gradients.findIndex((el) => el[0] === hashval);
    this.sequence = index;
    this.supply();
  }
});

window.addEventListener('load', () => {
  document.getElementById('step-next').onclick = pg.next.bind(pg);
  document.getElementById('step-previous').onclick = pg.previous.bind(pg);

  if(!window.location.hash) {
    pg.supply(pg.sequence = Math.abs(Math.floor(Math.random() * gradients.length - 1)));
  }
  pg.applyFromHashValue();
});

window.addEventListener("keydown", ({key}) => {
  switch(key) {
    case 'ArrowRight':
      return pg.next();
    case 'ArrowLeft':
      return pg.previous();
    case 'ArrowUp':
    case 'ArrowDown':
      return pg.switchBackgroundPosition();
  }
});
